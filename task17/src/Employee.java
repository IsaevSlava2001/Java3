public class Employee
{
    private String EmployeeName;
    private String EmployeeID;
    private String EmployeeStage;
    private String Employeework;
    private String Employeemoney;

    public String getEmployeeID()
    {
        return EmployeeID;
    }
    public void setEmployeeID(String EmployeeID)
    {
        this.EmployeeID=EmployeeID;
    }
    public String getEmployeeName()
    {
        return EmployeeName;
    }
    public void setEmployeeName(String EmployeeName)
    {
        this.EmployeeName=EmployeeName;
    }
    public String getEmployeeStage()
    {
        return EmployeeStage;
    }
    public void setEmployeeStage(String EmployeeStage)
    {
        this.EmployeeStage=EmployeeStage;
    }
    public String getEmployeework()
    {
        return Employeework;
    }
    public void setEmployeework(String Employeework)
    {
        this.Employeework=Employeework;
    }
    public String getEmployeemoney()
    {
        return Employeemoney;
    }
    public void setEmployeemoney(String Employeemoney)
    {
        this.Employeemoney=Employeemoney;
    }
}
